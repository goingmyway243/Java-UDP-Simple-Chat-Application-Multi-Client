    private void sendButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_sendButtonActionPerformed
        String message = messageTextArea.getText();

        if (message.isEmpty()) {
            return;
        }

        messageTextArea.setText("");
        try {
            contentListModel.addElement(message);
            sendPacket.setData((this.chatName + ": " + message).getBytes());
            connectSocket.send(sendPacket);
        } catch (Exception ex) {
            System.out.println("Send error: " + ex);
        }

    }//GEN-LAST:event_sendButtonActionPerformed

    private void exitButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_exitButtonActionPerformed
        try {
            sendPacket.setData(("/ext-" + this.chatName).getBytes());
            connectSocket.send(sendPacket);
        } catch (Exception ex) {
            System.out.println("Exit error: " + ex);;
        }
        connectSocket.close();
        System.exit(0);
    }//GEN-LAST:event_exitButtonActionPerformed


