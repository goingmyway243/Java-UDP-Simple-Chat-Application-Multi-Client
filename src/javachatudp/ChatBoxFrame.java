    public ChatBoxFrame(DatagramSocket socket, DatagramPacket packet, String id, String name, String idRoom) throws SocketException {
        initComponents();
        this.chatName = name;
        this.idRoom = idRoom;
        this.id = id;
        this.sendPacket = packet;
        this.connectSocket = socket;

        roomNameLabel.setText("Phòng chat " + this.idRoom);
        nickNameLabel.setText("Tên chat: " + this.chatName);
        idLabel.setText("#" + this.id);
        contentListModel = new DefaultListModel();
        chatContentList.setModel(contentListModel);
        chatContentList.setCellRenderer(new MyListCellRenderer(this.chatName));

        Thread readThread = new Thread(() -> {
            try {
                while (true) {
                    byte[] buff = new byte[1024];
                    DatagramPacket din = new DatagramPacket(buff, buff.length);
                    connectSocket.receive(din);
                    String recvMessage = new String(din.getData()).trim();
                    contentListModel.addElement(recvMessage);
                }
            } catch (Exception ex) {
                System.out.println("Receive error: " + ex);
            }
        });
        readThread.start();
    }

